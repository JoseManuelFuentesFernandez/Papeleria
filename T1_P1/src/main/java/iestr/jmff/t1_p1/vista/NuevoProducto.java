    /**
     * Este método se llama cuando se hace clic en el botón "Nuevo Producto".
     * Valida los campos ingresados y crea un nuevo objeto Tablapapeleria para almacenar en la base de datos.
     * @param evt Evento de acción.
     */
    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // TODO add your handling code here:
        //Control de campos vacíos
        if(txf1.getText().equals("") || txf2.getText().equals("") || txf3.getText().equals("") || txf4.getText().isBlank()){
            Menu.error("Completa los campos");
            return;
        }
        
        //Control de si ya existe ese código
        if(!Menu.codigoLibre(txf1.getText())){
            Menu.error("Ese código ya está en uso");
            return;
        }
        
        //Control de Double en el precio
        if(!Menu.esDouble(txf4.getText())){
            Menu.error("El precio no es un valor válido\nEjemplos válidos: 1000.00 / 1000");
            return;
        }
        
        Tablapapeleria nuevo = new Tablapapeleria();
        nuevo.setCodigo(txf1.getText());
        nuevo.setProducto(txf2.getText());
        nuevo.setFamilia(txf3.getText());
        nuevo.setPrecio(Double.valueOf(txf4.getText()));
        
        producto.create(nuevo);
        productos.add(nuevo);
        
        Menu.exito("Producto creado con éxito");
    }//GEN-LAST:event_btn1ActionPerformed

